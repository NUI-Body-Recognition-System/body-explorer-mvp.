let currentLocale = 'en';

export const i18n = {
  t(key, ...args) {
    const dict = DICTIONARY[currentLocale];
    let str = dict[key] || key;
    args.forEach((arg, i) => {
      str = str.replace(`{${i}}`, arg);
    });
    return str;
  },
  
  setLocale(lang) {
    if (DICTIONARY[lang]) {
      currentLocale = lang;
      eventBus.emit('i18n:change', { lang });
    }
  },
  
  getLocale() {
    return currentLocale;
  }
};

